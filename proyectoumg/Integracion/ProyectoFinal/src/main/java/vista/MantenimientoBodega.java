    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel(); //estamos creando el modelo, un grid
        modelo.addColumn("Pkid");
        modelo.addColumn("Tipo_Bodega");
        modelo.addColumn("Nombre_bodega");
        modelo.addColumn("Direccion");
        modelo.addColumn("Estado");
        BodegaDAO bodegaDAO = new BodegaDAO();
        List<Bodega> bodegas = bodegaDAO.select(); //vendedores=alumnos, alumno=vendedor
        tablaSedes.setModel(modelo);
        String[] dato = new String[5];
        for (int i = 0; i < bodegas.size(); i++) {
            dato[0] = bodegas.get(i).getPkid();
            dato[1] = bodegas.get(i).getTipo_bodega();
            dato[2] = bodegas.get(i).getNombre_bodega();
            dato[3] = bodegas.get(i).getDireccion();
            dato[4] = bodegas.get(i).getEstado();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

